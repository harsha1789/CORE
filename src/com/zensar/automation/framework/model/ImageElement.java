package com.zensar.automation.framework.model;

public class ImageElement {

	int imageX;
	
	int imageY;
	
	int findImageWidth;
	
	int findImageHeight;

	

	public int getImageX() {
		return imageX;
	}

	public void setImageX(int imageX) {
		this.imageX = imageX;
	}

	public int getImageY() {
		return imageY;
	}

	public void setImageY(int imageY) {
		this.imageY = imageY;
	}

	public int getFindImageWidth() {
		return findImageWidth;
	}

	public void setFindImageWidth(int findImageWidth) {
		this.findImageWidth = findImageWidth;
	}

	public int getFindImageHeight() {
		return findImageHeight;
	}

	public void setFindImageHeight(int findImageHeight) {
		this.findImageHeight = findImageHeight;
	}

	
	
}
